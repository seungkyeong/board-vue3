    //Î°úÎìúÏãú Í≤åÏãúÎ¨º Ï°∞Ìöå
    onMounted(() => {
      getBoardDetail()
    })

    /* ÏóÖÎ°úÎìúÌïú ÌååÏùº ÌÅ¥Î¶≠Ïãú ÌååÏùº ÎØ∏Î¶¨Î≥¥Í∏∞ */
    const imgPreview = (url) => {
      previewImage.value = url
      dialogVisible.value = true
    }

    /* Ïù¥ÎØ∏ÏßÄÎ•º Upload Ïª¥Ìè¨ÎÑåÌä∏ ÌëúÏãúÏö© Í∞ùÏ≤¥Î°ú Î≥ÄÌôò */
    const toUploadFileList = () => {
      //Í∏∞Ï°¥ Ïù¥ÎØ∏ÏßÄÎ•º Upload Ïª¥Ìè¨ÎÑåÌä∏ ÌëúÏãúÏö© Í∞ùÏ≤¥Î°ú Î≥ÄÌôò
      const uploadList = form.imgPath.map((url, index) => {
        const fileName = url.split('/').pop()
        return {
          name: fileName,
          url: url,
          uid: `preloaded-${index}`,
          status: 'ready',
          raw: null,
        }
      })

      files.value = uploadList
    }

    /* Í≤åÏãúÍ∏Ä ÏàòÏ†ï Î≤ÑÌäº ÌÅ¥Î¶≠Ïãú, ÏàòÏ†ï ÌéòÏù¥ÏßÄÎ°ú Ïù¥Îèô */
    /* Í≤åÏãúÍ∏Ä ÏàòÏ†ï Î≤ÑÌäº ÌÅ¥Î¶≠Ïãú, Í≤åÏãúÍ∏Ä ÏàòÏ†ï */
    const modifyBoard = async () => {
      //Í≤åÏãúÍ∏Ä ÏàòÏ†ï ÌÜ†Í∏ÄÎêú Í≤ΩÏö∞(Í≤åÏãúÍ∏Ä ÏàòÏ†ï)
      if (!editBoardToggle.value) {
        if (!form.title.trim() || !form.content.trim()) {
          //Î™®Îì† ÌïÑÎìú ÏûÖÎ†• ÌôïÏù∏
          await showAlertBox(
            MESSAGES.REQUIRE_ALL_FIELDS,
            MESSAGES.WARNING
          ).catch(() => {})
        } else {
          //Ïã†Í∑ú ÏóÖÎ°úÎìú ÌååÏùº Ï∂îÏ∂ú(rawÍ∞Ä ÏûàÎäî ÌååÏùº)
          const uploadFiles = files.value.filter((file) => file.raw != null)

          //Ïã†Í∑ú ÏóÖÎ°úÎìú ÌååÏùºÏù¥ ÏûàÎäî Í≤ΩÏö∞
          if (uploadFiles.length > 0) {
            // FormData ÏÉùÏÑ±
            const formData = new FormData()
            uploadFiles.forEach((file) => formData.append('files', file.raw))

            //S3ÏóêÏÑú PresignedURL Î∞úÍ∏â
            const presignedURLs = await boardAPI.getPresignedURL(formData)

            const imgPath = []
            for (let i = 0; i < presignedURLs.length; i++) {
              //S3Ïóê ÌååÏùº ÏóÖÎ°úÎìú
              const response = await boardAPI.uploadFile(
                presignedURLs[i],
                uploadFiles[i].raw
              )

              //ÌååÏùº Ïù¥Î¶ÑÎßå Ï∂îÏ∂úÌïòÏó¨ imgPathÏóê Ï†ÄÏû•
              imgPath.push(decodeURIComponent(response.split('/')[3]))
            }

            //ÏµúÏ¢Ö imgPathÏóê Í∏∞Ï°¥ ÌååÏùºÎ™ÖÍ≥º Ïã†Í∑ú ÏóÖÎ°úÎìú ÌååÏùºÎ™Ö Ï†ÄÏû•
            form.imgPath = [
              ...files.value
                .filter((file) => !file.raw)
                .map((file) => file.url.split('/').pop()), // Í∏∞Ï°¥ ÌååÏùºÎ™Ö
              ...imgPath, //Ïã†Í∑ú ÏóÖÎ°úÎìú ÌååÏùºÎ™Ö
            ]
          }

          //ÏÇ≠Ï†úÌï† Ïù¥ÎØ∏ÏßÄÍ∞Ä ÏûàÎäî Í≤ΩÏö∞, S3ÏóêÏÑú Ïù¥ÎØ∏ÏßÄ ÏÇ≠Ï†ú
          if (deletedImages.value.length > 0) {
            await boardAPI.deleteFiles({ keys: deletedImages.value })
          }

          //***Ïù¥Í±∞ ÏóÜÏï†Í≥† ÌïúÎ≤à Îçî ÌôïÏù∏Ìï¥Î≥¥***
          form.imgPath = [
            ...form.imgPath.map((url) => url.split('/').pop()), // Í∏∞Ï°¥ ÌååÏùºÎ™ÖÎßå Ï∂îÏ∂ú
          ]

          //Ï†ÄÏû• API Ìò∏Ï∂ú
          const response = await boardAPI.createBoard(form)

          if (response.success) {
            await showAlertBox(MESSAGES.SUCCESS_SAVE, MESSAGES.SUCCESS)
              .then(() => {
                form.imgPath = []
                deletedImages.value = []
                getBoardDetail()
                editBoardToggle.value = true
              })
              .catch(() => {})
          } else {
            await showAlertBox(response.message, MESSAGES.ERROR).catch(() => {})
          }
        }
      } else {
        //Í≤åÏãúÍ∏Ä ÏàòÏ†ï ÌÜ†Í∏Ä ÏïàÎêú Í≤ΩÏö∞(Í≤åÏãúÍ∏Ä ÏÉÅÏÑ∏Î≥¥Í∏∞)
        if (form.userId == userId) {
          //ÏÇ¨Ïö©ÏûêÍ∞Ä ÏûëÏÑ±ÏûêÏù∏ Í≤ΩÏö∞, ÏàòÏ†ï
          //ÌååÏùº Î™©Î°ùÏùÑ Upload Ïª¥Ìè¨ÎÑåÌä∏ Í∞ùÏ≤¥Î°ú Î≥ÄÌôò
          toUploadFileList()

          //Í≤åÏãúÍ∏Ä ÏàòÏ†ï ÌôîÎ©¥ÏúºÎ°ú ÌÜ†Í∏Ä
          editBoardToggle.value = false
        } else {
          //ÏÇ¨Ïö©ÏûêÍ∞Ä ÏûëÏÑ±ÏûêÍ∞Ä ÏïÑÎãå Í≤ΩÏö∞
          await showAlertBox(MESSAGES.AUTHOR_MISMATCH, MESSAGES.WARNING).catch(
            () => {}
          )
        }
      }
    }

    //Ï¢ãÏïÑÏöî Î≤ÑÌäº ÌÅ¥Î¶≠Ïãú Redis Ï¢ãÏïÑÏöî Count Ï¶ùÍ∞Ä
    const toggleLikeBtn = async () => {
      if (isLiked.value) {
        //Ï¢ãÏïÑÏöî ÎàÑÎ•∏ ÏÉÅÌô© -> Ï∑®ÏÜåÏù∏ Í≤ΩÏö∞
        isLiked.value = false
        await boardAPI.updateLike({
          action: 'unLike',
          boardSysNo: form.sysNo,
          userId: userId,
          userSysNo: userSysNo,
        })
      } else {
        //Ï¢ãÏïÑÏöî ÏïàÎàÑÎ•∏ ÏÉÅÌô© -> Ï¢ãÏïÑÏöî ÎàÑÎ•∏ Í≤ΩÏö∞
        isLiked.value = true
        await boardAPI.updateLike({
          action: 'like',
          boardSysNo: form.sysNo,
          userId: userId,
          userSysNo: userSysNo,
        })
      }
    }

    //ÎåìÍ∏Ä Îì±Î°ù Î≤ÑÌäº ÌÅ¥Î¶≠Ïãú ÎåìÍ∏Ä Ï†ÄÏû•
    const createComment = async (comment = null) => {
      let response
      //ÎåìÍ∏Ä, ÎåÄÎåìÍ∏Ä ÏàòÏ†ï
      if (comment != null && comment.editCommentToggle) {
        newReply.comment = comment.editText
        newReply.sysNo = comment.sysNo
        newReply.parSysNo = comment.parSysNo
        response = await boardAPI.createComment(newReply)
      } else if (comment != null) {
        //ÎåÄÎåìÍ∏Ä Ïã†Í∑ú Îì±Î°ùÏù∏ Í≤ΩÏö∞
        newReply.parSysNo = comment.sysNo
        newReply.boardSysNo = form.sysNo
        newReply.comment = replyInputs[comment.sysNo] || '' // Ìï¥Îãπ ÎåìÍ∏ÄÏùò ÏûÖÎ†•Í∞í ÏÇ¨Ïö©

        response = await boardAPI.createComment(newReply)
      } else {
        //ÎåìÍ∏Ä Ïã†Í∑ú Îì±Î°ùÏù∏ Í≤ΩÏö∞
        response = await boardAPI.createComment(newComment)
      }

      if (response.success) {
        await showAlertBox(MESSAGES.SUCCESS_SAVE_COMMENT, MESSAGES.SUCCESS)
          .then(() => {
            newComment.comment = ''
            Object.keys(replyInputs).forEach((key) => (replyInputs[key] = '')) // Î™®Îì† ÎåÄÎåìÍ∏Ä ÏûÖÎ†•Ï∞Ω Ï¥àÍ∏∞Ìôî
            newReply.comment = ''
            getBoardDetail()
          })
