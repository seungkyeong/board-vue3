      const fileName = url.split('/').pop()
      if (!deletedImages.value.includes(fileName)) {
        deletedImages.value.push(fileName)
      }

      //form.imgPathÏóêÏÑú Ï†úÍ±∞
      form.imgPath = form.imgPath.filter((imgUrl) => imgUrl !== url)
    }

    return {
