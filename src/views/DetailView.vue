    //ÎåìÍ∏Ä ÏàòÏ†ï
    const editComment = async (comment) => {
      if (comment.userId == userId) {
        comment.editCommentToggle = true // ÏÑ†ÌÉùÎêú ÎåìÍ∏ÄÎßå ÏàòÏ†ï Î™®ÎìúÎ°ú
        comment.editText = comment.comment
      } else {
